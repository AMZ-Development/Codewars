//We will receive a number, it can be integer or float
//The number should be converted and return as a string

function numberToString(num) {
    return num.toString();
}

//Tests
console.log(numberToString(23)); //Output: '23'
console.log(numberToString(0)); //Output: '0'
console.log(numberToString(-23)); //Output: '-23'