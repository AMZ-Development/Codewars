//PREP
//We will receive an integer
//We have to return the opposite of that number(if negative return positive, or viceversa)


let opposite = number => number * -1

//TESTS
console.log(opposite(1)); //Output: -1
console.log(opposite(45)); //Output: -45
console.log(opposite(-10)); //Output: 10
console.log(opposite(-21)); //Output: 21
