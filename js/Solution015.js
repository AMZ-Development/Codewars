//PREP
//We will receive an integer
//We will have tu return the double of that integer
//The integer can be positive, negative or 0

//function
function doubleInteger(i){
//double the integer
    i *= 2;
//return the result
    return i;
}

console.log(doubleInteger(2));  //output: 4
console.log(doubleInteger(-2));  //output: -4
console.log(doubleInteger(0));  //output: 0
console.log(doubleInteger(10));  //output: 20

